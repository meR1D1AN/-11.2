def title_text(text):
    """
    Функция, которая принимает на вход строку и возвращает её со всеми заглавными буквами
    """
    return text.upper()


def title_word(text):
    """
    Функция, которая делает заглавными первые буквы каждого слова в строке, поступившей на вход функции
    """
    return text.title()
